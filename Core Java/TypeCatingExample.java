class TypeCatingExample 
{
    public static void main(String[] args) 
    {
        double d = 3.14;
        int i = (int) d;
        System.out.println("Double value: "+d);
        System.out.println("Integer value after type casting: "+i);
        int i1 = 100;
        double d1 = i1;
        System.out.println("Integer value: "+i1);
        System.out.println("Double value after type casting: "+d1);
    }
}
